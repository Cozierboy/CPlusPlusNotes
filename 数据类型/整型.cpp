#include <iostream>

int main(int argc, char *argv[])
{
  //整型
  //1. 短整型
  short num1 = 32768;

  //2. 整型
  int num2 = 32768;

  //3. 长整型
  long num3 = 10;

  //4. 长长整型
  long long num4 =10;

  std::cout << "num1 = " << num1 << std::endl;
  std::cout << "num2 = " << num2 << std::endl;
  std::cout << "num3 = " << num3 << std::endl;
  std::cout << "num4 = " << num4 << std::endl;
  return 0;
}
